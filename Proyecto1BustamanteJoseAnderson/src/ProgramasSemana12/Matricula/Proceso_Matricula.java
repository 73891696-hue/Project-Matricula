    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        double vacante;
        char turno=0;
        String textoco1="";
       
        try{ 
            vacante = Double.parseDouble(this.jTextField1.getText());
            
            if(this.jbotonMñn.isSelected()==true)
            {
                turno='M';
                textoco1="Mañana";}
            else
            if(this.jbotonTarde.isSelected()==true)
            {
                turno='T';
                textoco1="Tarde";  }
            
        if (vacante<0) 
        {
                    JOptionPane.showMessageDialog(null,"Error no se acepta numero negativo. Vuelva a ingresar");
                    jTextField1.setText("");  
                } 
        else{
               this.jTextArea1.setText("Vacantes: "+vacante+"\n");
               this.jTextArea1.append("Grado: "+this.jComboBox1.getSelectedItem()+"\n");
               this.jTextArea1.append("Seccion: "+this.jComboBox2.getSelectedItem()+"\n" );
                this.jTextArea1.append("Turno: "+textoco1 +"\n");
                
                
              }
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null,"Ocurrio un error del tipo:"+ e.toString());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

